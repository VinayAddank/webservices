package org.rta.core.model.certificate;

public class FitnessCategoryModel {
	
	private Boolean ghatRoad;
	private Integer fitnessCategory;
	private Long nextInspectionDate;
	
	public Boolean getGhatRoad() {
		return ghatRoad;
	}
	public void setGhatRoad(Boolean ghatRoad) {
		this.ghatRoad = ghatRoad;
	}
	public Integer getFitnessCategory() {
		return fitnessCategory;
	}
	public void setFitnessCategory(Integer fitnessCategory) {
		this.fitnessCategory = fitnessCategory;
	}
	public Long getNextInspectionDate() {
		return nextInspectionDate;
	}
	public void setNextInspectionDate(Long nextInspectionDate) {
		this.nextInspectionDate = nextInspectionDate;
	}
	
}
