package org.rta.core.model.certificate;

public class OtherVehicleDetailModel {
	
	private String taxPending;
	private String theftCase;
	private String sessionActPending;
	private String transportCaseGoods;
	
	public String getTaxPending() {
		return taxPending;
	}
	public void setTaxPending(String taxPending) {
		this.taxPending = taxPending;
	}
	public String getTheftCase() {
		return theftCase;
	}
	public void setTheftCase(String theftCase) {
		this.theftCase = theftCase;
	}
	public String getSessionActPending() {
		return sessionActPending;
	}
	public void setSessionActPending(String sessionActPending) {
		this.sessionActPending = sessionActPending;
	}
	public String getTransportCaseGoods() {
		return transportCaseGoods;
	}
	public void setTransportCaseGoods(String transportCaseGoods) {
		this.transportCaseGoods = transportCaseGoods;
	}
	
}
