package org.rta.core.model.export;

public class IibExportModel {
	
	private String regType;
	private String regNumber;
	private String chassisNumber;
	private String engineNumber;
	private String makerName;
	private String modelName;
	private String cc;
	private String gvw;
	private String hypothecation;
	private String regStartDate;
	private String regEndDate;
	
	public String getRegNumber() {
		return regNumber;
	}
	public void setRegNumber(String regNumber) {
		this.regNumber = regNumber;
	}
	public String getChassisNumber() {
		return chassisNumber;
	}
	public void setChassisNumber(String chassisNumber) {
		this.chassisNumber = chassisNumber;
	}
	public String getEngineNumber() {
		return engineNumber;
	}
	public void setEngineNumber(String engineNumber) {
		this.engineNumber = engineNumber;
	}
	public String getMakerName() {
		return makerName;
	}
	public void setMakerName(String makerName) {
		this.makerName = makerName;
	}
	public String getModelName() {
		return modelName;
	}
	public void setModelName(String modelName) {
		this.modelName = modelName;
	}
	public String getCc() {
		return cc;
	}
	public void setCc(String string) {
		this.cc = string;
	}
	public String getGvw() {
		return gvw;
	}
	public void setGvw(String gvw) {
		this.gvw = gvw;
	}
	public String getHypothecation() {
		return hypothecation;
	}
	public void setHypothecation(String hypothecation) {
		this.hypothecation = hypothecation;
	}
	public String getRegStartDate() {
		return regStartDate;
	}
	public void setRegStartDate(String regStartDate) {
		this.regStartDate = regStartDate;
	}
	public String getRegEndDate() {
		return regEndDate;
	}
	public void setRegEndDate(String regEndDate) {
		this.regEndDate = regEndDate;
	}
	public String getRegType() {
		return regType;
	}
	public void setRegType(String regType) {
		this.regType = regType;
	}
	
	
}
	