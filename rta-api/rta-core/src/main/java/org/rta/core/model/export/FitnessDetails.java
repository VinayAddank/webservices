public class FitnessDetails {
	
	private String fcNumber;
	private String fcIssuedBy;
	private String fcIssuedDate;
	private String fcValidFromDate;
	private String fcValidToDate;
	private String fcApprovedBy;
	private String fcChallanNo;
	
	
	public String getFcNumber() {
		return fcNumber;
	}
	public void setFcNumber(String fcNumber) {
		this.fcNumber = fcNumber;
	}
	public String getFcIssuedBy() {
		return fcIssuedBy;
	}
	public void setFcIssuedBy(String fcIssuedBy) {
		this.fcIssuedBy = fcIssuedBy;
	}
	public String getFcIssuedDate() {
		return fcIssuedDate;
	}
	public void setFcIssuedDate(String fcIssuedDate) {
		this.fcIssuedDate = fcIssuedDate;
	}
	public String getFcValidFromDate() {
		return fcValidFromDate;
	}
	public void setFcValidFromDate(String fcValidFromDate) {
		this.fcValidFromDate = fcValidFromDate;
	}
	public String getFcValidToDate() {
		return fcValidToDate;
	}
	public void setFcValidToDate(String fcValidToDate) {
		this.fcValidToDate = fcValidToDate;
	}
	public String getFcApprovedBy() {
		return fcApprovedBy;
	}
	public void setFcApprovedBy(String fcApprovedBy) {
		this.fcApprovedBy = fcApprovedBy;
	}
	public String getFcChallanNo() {
		return fcChallanNo;
	}
	public void setFcChallanNo(String fcChallanNo) {
		this.fcChallanNo = fcChallanNo;
	}
	
}
